--Create table for which we will be creating trigger
CREATE TABLE FLOWERS
( FLOWER_NAME VARCHAR2(20));

--Trigger Program
SET SERVEROUTPUT ON;

CREATE OR REPLACE TRIGGER FLOWERS_BT
BEFORE INSERT OR DELETE OR UPDATE ON FLOWERS
FOR EACH ROW
ENABLE
DECLARE
  V_USER VARCHAR2(10);
BEGIN
  SELECT USER INTO V_USER FROM DUAL;
  IF INSERTING THEN
    DBMS_OUTPUT.PUT_LINE(V_USER || ' IS INSERTING A ROW');
  ELSIF DELETING THEN
    DBMS_OUTPUT.PUT_LINE(V_USER || ' IS DELETING A ROW');
  ELSIF UPDATING THEN
    DBMS_OUTPUT.PUT_LINE(V_USER || 'IS UPDATING A ROW');
  ENDIF;
END;
/

--DML on which you can check if trigger is working or not

INSERT INTO FLOWERS VALUES ('MARIGOLD');
COMMIT;
UPDATE FLOWERS SET FLOWER_NAME ='SUNFLOWER' WHERE FLOWER_NAME='MARIGOLD');
COMMIT;
DELETE FROM FLOWERS WHERE FLOWER_NAME='SUNFLOWER';
COMMIT;
